function legacyCoverage(activity: Antibiotic["activity"]) {
  return {
    coversMrsa: activity.mrsa === "あり",
    coversPseudomonas: activity.pseudomonas === "あり",
    coversAnaerobes: activity.anaerobes === "あり",
    coversAtypicals: activity.atypicals === "あり",
  };
}

function makeAntibiotic(input: Omit<Antibiotic, "coversMrsa" | "coversPseudomonas" | "coversAnaerobes" | "coversAtypicals" | "coverage" | "penetration" | "representativeIndications" | "pmdaCheckedAt" | "dosing">): Antibiotic {
  const coverage = legacyCoverage(input.activity);
  return {
    ...input,
    coverage: {
      mrsa: coverage.coversMrsa,
      pseudomonas: coverage.coversPseudomonas,
      esbl: input.esblPosition.includes("候補") ? "limited" : input.esblPosition.includes("標準") ? true : false,
      anaerobes: coverage.coversAnaerobes,
      atypicals: coverage.coversAtypicals,
    },
    penetration: {
      csf: input.tissuePenetration.csf,
      bile: input.tissuePenetration.bile,
      prostate: input.tissuePenetration.prostate,
      bone: input.tissuePenetration.bone,
    },
    representativeIndications: input.domesticApprovedIndications,
    pmdaCheckedAt: input.checkedAt,
    sources: input.sources,
    dosing: dosing(input.tdm.includes("必要") || input.tdm.includes("推奨") ? "必要" : "不要候補"),
    ...coverage,
  };
}

